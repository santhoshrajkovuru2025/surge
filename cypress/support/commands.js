// To use the custom commands use Faker API
import { faker } from '@faker-js/faker';

Cypress.Commands.add("generateUser", function () {
    return {
        "firstName": faker.person.firstName(),
        "email": faker.internet.email(),
        "Phone": faker.phone.number()
    }
})